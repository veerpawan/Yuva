package yuvajobopps.niit.org.yuva;


public class DataObject {
    private String mText1;
    private String mText2;
    private String mText3;
    private String mText4;
    private String mText5;

    public DataObject(String mText1, String mText2, String mText3, String mText4, String mText5) {
        this.mText1 = mText1;
        this.mText2 = mText2;
        this.mText3 = mText3;
        this.mText4 = mText4;
        this.mText5 = mText5;
    }

    public String getmText1() {
        return mText1;
    }

    public void setmText1(String mText1) {
        this.mText1 = mText1;
    }

    public String getmText2() {
        return mText2;
    }

    public void setmText2(String mText2) {
        this.mText2 = mText2;
    }
    public String getmText3() {
        return mText3;
    }

    public void setmText3(String mText3) {
        this.mText3 = mText3;
    }

    public String getmText4() {
        return mText4;
    }

    public void setmText4(String mText4) {
        this.mText4 = mText4;
    }

    public String getmText5() {
        return mText5;
    }

    public void setmText5(String mText5) {
        this.mText5 = mText5;
    }

}
